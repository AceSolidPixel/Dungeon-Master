/** The point class is uesed by the map class to remove and add characters on the map */
public class Point
{
/** Gives the x coordinate on the map */
  public int x;
/** Gives the y coordinate on the map */
  public int y;
/**
* Constructor for the Point class. Initializes the x and y coordinates
*/
  public Point(int xCoor, int yCoor)
  {
    x = xCoor;
    y = yCoor;
  }
}